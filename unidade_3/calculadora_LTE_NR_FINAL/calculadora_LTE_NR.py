    def calculatetpNR(self):
        NumCCs = int(self.CCBox.currentText())
        MIMOLayers = int(MIMOTALBE[self.NRMIMOBox.currentText()])
        NRMCSindex = int(self.NRMCSBox.currentIndex())
        if self.TABLEBox.currentIndex() == 0:
            NumBitsMod = int(NRMCSTABLE1[int(self.NRMCSBox.currentText())]['Q'])
            coderate = int(NRMCSTABLE1[int(self.NRMCSBox.currentText())]['coderate'])
        elif self.TABLEBox.currentIndex() == 1:
            NumBitsMod = int(NRMCSTABLE2[int(self.NRMCSBox.currentText())]['Q'])
            coderate = int(NRMCSTABLE2[int(self.NRMCSBox.currentText())]['coderate'])
        elif self.TABLEBox.currentIndex() == 2:
            NumBitsMod = int(NRMCSTABLE3[int(self.NRMCSBox.currentText())]['Q'])
            coderate = int(NRMCSTABLE3[int(self.NRMCSBox.currentText())]['coderate'])
        ScalingFactor = float(self.NRSFBox.currentText())
        if self.NRFRBox.currentIndex() == 0:
            NumPRB = FR1PRBTABLE[int(self.NRSCSBox.currentIndex())][self.NRBWBox.currentText()]
        else:
            NumPRB = FR2PRBTABLE[int(self.NRSCSBox.currentIndex())][self.NRBWBox.currentText()]
        numerology = NUMEROLOGYTABLE[self.NRSCSBox.currentText()]
        SymbolTime = (10**(-3))/(14*(2**numerology))
        OH = OHTABLE[self.DirectionBox.currentIndex()][self.NRFRBox.currentText()]
            
        TputNR = ((10**-6)* NumCCs * (MIMOLayers * NumBitsMod * (coderate/1024) * ScalingFactor * ((NumPRB*12)/SymbolTime) * (1-OH)))
        self.NRTputLabel.setText(str(round(TputNR,2)))



